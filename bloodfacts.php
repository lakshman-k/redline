   <h1>Blood Facts</h1>
   <li>	Blood makes up around 7% of the weight of a human body.</li>
<li>Blood contains red blood cells, white blood cells and platelets.</li>
<li>These blood cells float in a yellow liquid called blood plasma. Blood plasma is made up of 90% water and also contains various nutrients, electrolytes, gases, proteins, glucose and hormones.</li>
<li>Blood plasma can be separated from the cells by spinning blood in a device known as a centrifuge until the cells collect at the bottom of the tube.</li>
<li>Red blood cells have the important job of carrying oxygen around the body. They also contain a protein called hemoglobin. Hemoglobin contains iron which combines with oxygen to give hemoglobin and our blood, a red color.</li>
<li>Red blood cells develop in bone marrow and circulate in the body for around 120 days.</li>
<li>White blood cells are an important part of the body’s immune system. They defend against certain bacteria, viruses, cancer cells, infectious diseases and other unwanted materials.</li>
<li>Platelets help blood clot in order to limit bleeding when your skin is cut. Blood clots can occasionally have negative effects, if they form in blood vessels going to the brain they can cause a stroke while clotting in a blood vessel going to the heart can lead to a heart attack.</li>
<li>As well as delivering important substances to our cells, blood also helps take away unwanted waste products.</li>
<li>Grouping human blood types can be a difficult process and there are currently around 30 recognized blood types (or blood groups). You might be familiar with the more simplified “ABO” system which categorizes blood types under O, A, B and AB. Do you know which blood type you are?</li>
<li>Many generous humans around the world give blood donations every year. This blood is used in important blood transfusions or made into medication.</li>
<li>There are strict rules that limit the number of people who can volunteer blood donations. These include screening processes that test for diseases that could be transmitted by a blood transfusion as well as ensuring recovery time for the donor’s body to replace its own blood.</li>
<li>	One of the principal signs of life for humans is blood pressure, this is the measure of pressure that circulating blood has on the walls of blood vessels. Blood pressure is usually taken from a person’s upper arm. Although averages vary from person to person, a general human being is known to have a normal blood pressure of around 112/64 mmHg. High blood pressure can increase the risk of a stroke or heart attack.</li>

